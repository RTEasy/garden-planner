// Helper function to find a seed by ID
export const getSeedById = (id: string): Seed | undefined => {
  return seedCatalog.find(seed => seed.id === id);
};

// Helper function to filter seeds by type
export const getSeedsByType = (type: PlantType): Seed[] => {
  return seedCatalog.filter(seed => seed.plantType === type);
};

// Helper function to search seeds
export const searchSeeds = (query: string): Seed[] => {
  const lowerQuery = query.toLowerCase();
  return seedCatalog.filter(seed =>
    seed.commonName.toLowerCase().includes(lowerQuery) ||
    seed.cultivar.toLowerCase().includes(lowerQuery) ||
    seed.genusSpecies.toLowerCase().includes(lowerQuery)
  );
};
