export interface ZipLookupResult {
  zone: string | null;
  error: string | null;
}

export function lookupZoneByZip(zipCode: string): ZipLookupResult {
  // Clean the zip code
  const cleanZip = zipCode.replace(/\D/g, '').slice(0, 5);

  if (cleanZip.length < 3) {
    return { zone: null, error: 'Enter at least 3 digits' };
  }

  const prefix = cleanZip.slice(0, 3);
  const zone = ZIP_PREFIX_TO_ZONE[prefix];

  if (zone) {
    return { zone, error: null };
  }

  return { zone: null, error: 'Zone not found for this zip code' };
}

